# Make a program with 2 numbers saved in separate variables a and b, then print the result for each of the following:
# Addition
# Subtraction
# Division
# Multiplication
# Exponent (Power)
# Modulus
# Floor division
a=int(input('Input a first number '))
b=int(input('Input a second number'))
print('Result of addition is',a+b)
print('Result of subtruction is',a-b)
print('Result of division is',a/b)
print('Result of multiplication is',a*b)
print('Result of exponent is',a**b)
print('Result of modulus is',a%b)
print('Result of floor division is',a//b)


